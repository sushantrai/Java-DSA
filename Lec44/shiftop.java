package Lec44;

public class shiftop {
    public static void main(String[] args) {
        int n=7;
        System.out.println(n>>2);//right shift
        int n1=-5;
        System.out.println(n1>>2);
        System.out.println(n1<<2);
        System.out.println(n<<2);//left shift
        int n2=1;
        System.out.println(n2<<4);
    }
}
